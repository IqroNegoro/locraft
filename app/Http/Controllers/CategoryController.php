    public function search(Request $request) {
        try {
            $query = $request->input('q');
            if (!$query) {
                return response()->json();
            }

            $categories = Category::where('name', 'like', '%' . $query . '%')
                ->orderBy('name')
                ->limit(10)
                ->get();

            return response()->json([
                'data' => $categories
            ]);
        } catch (\Throwable $e) {
            return response()->json([], 500);
        }
    }
